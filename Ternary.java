public class Ternary
{
    public static void main(String[] args)
    {
        String res=(2>3)?"True":"False";
        System.out.println(res);
        char c=(2>3)?'T':'F';
        System.out.println(c);
        boolean  result=(2>3)?true:false;
        System.out.println(result);
    }
}